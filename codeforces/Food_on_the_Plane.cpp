void solve(istream &in, ostream &out)
{
    long long a;
    char b;

    in >> a >> b;

    map<char, int> seatTime = {
        {'f', 1}, {'e', 2}, {'d', 3}, {'a', 4}, {'b', 5}, {'c', 6}};

    ll groups = (a - 1) / 4;
    ll time = groups * 16;

    ll left = (a - 1) % 4;
    if (left == 1 || left == 3)
    {

        time += 7;
    }

    time += seatTime[b];

    out << time;
}

